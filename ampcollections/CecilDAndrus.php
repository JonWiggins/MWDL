<!-- Article Text -->
Democrat Cecil D. Andrus (1931- ) served as U.S. Secretary of the Interior from 1977 until 1981 during the Carter administration. He was also elected to the Idaho Senate (1960, 1964, and 1964) and as Governor of Idaho (1970, 1974, 1986, and 1990), becoming the longest-serving governor in Idaho's history. This collection includes 2,000 photographs from the Cecil D. Andrus Papers (MSS 140) which span his personal and professional life, including his two terms as Governor of Idaho and his term as Secretary of the Interior under President Carter.
