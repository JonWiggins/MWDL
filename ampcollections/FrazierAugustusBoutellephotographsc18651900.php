<!-- Article Text -->
Frazier Augustus Boutelle (1840-1924) served in the US Army for 57 years, fighting in the Civil War, in Indian wars, and working as a recruiter in World War I. In 1889-1990 he was Superintendent of Yellowstone National Park. The collection consists of documentation of Boutelle's career: military scenes of the Indian Wars from 1870s to 1890s, images from the Philippines from 1898 to 1899, and the Yellowstone area 1889-1890.
