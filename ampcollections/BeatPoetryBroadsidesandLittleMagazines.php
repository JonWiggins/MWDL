<!-- Article Text -->
<p></p><br/><p>Beat Movement broadsides and posters in this collection were part of a revolution in self-publishing as artists and poets seized control over the dissemination of their work and took it directly to the people and the streets, dodging the strong forces of censorship that permeated 1950s America. Diverse in size, quality, and quantity – from mimeographs to fine letterpress print – broadsides have been important vehicles for political, social, and cultural commentary on a profusion of subjects including politics, free love, the anti-war movement, environmental consciousness, civil rights, free speech, and more. The broadsides are critical primary research materials, artfully documenting the dynamic social movements of the 1950s and 1960s.</p><br/></p>
