Capitol Commission Capitol Building Plans: First Floor Framing Plans
<!-- Article Text -->
These are the original building plans created by the capitol architect, Richard Kletting, for the construction of the Capitol Building. The series includes plans for framing, various construction details, columns and stone work, dome framing, foundation and footings, cross sections, and building elevations from various angles. The originals are blueprints, blueprints on linen, and tracings.
