</p><br/>The Bear River Watershed's geography, history, and development are the primary focus of this collection of images, maps, papers, and reports. Funded by grants from the Utah State University Water Initiative, this project digitizes selected materials cited in the Bear River Watershed Historical Bibliography, including photographs of the Bear River from the 1860s to the 1990s, manuscripts and records of local irrigation companies, research on the societal impact of reclamation development in the Bear River Basin, and the papers of Utah Governor George Dewey Clyde, who as a former USU Engineering Dean collected documentation on Bear River water conditions as far back as the 1920s.</p><br/>
