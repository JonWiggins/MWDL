This digital collection focuses on the scrapbooks and yearbooks from the elementary, junior high and high schools, past and present, of Orem, Utah. Several of the schools-Spencer School, Lincoln High School, and Lincoln Junior High School-no longer exist. <br/>The schools included in this collection, and the years of operation, are:<br/>Spencer School, 1883-1989<br/>Lincoln High School, 1912-1921<br/>Lincoln Junior High School, 1921-1975<br/>Orem High School, 1956-<br/>Mountain View High School, 1980-<br/>Timpanogos High School, 1996-<br/>Orem Junior High School, 1963-2010<br/>Lakeridge Junior High School, 1975-<br/>Canyon View Junior High School, 1985-1989<br/>The most recent yearbooks are subject to a five to seven year delay from the original publication date. Missing yearbooks from these schools as well as materials from additional schools may be added in the future as they become available.
