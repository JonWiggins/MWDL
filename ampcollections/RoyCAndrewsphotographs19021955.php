Roy C. Andrews was a school teacher and administrator in Michigan, Texas, Arkansas, Washington and Oregon. He was a chemistry instructor at the University of Oregon from 1935 until his retirement in 1950. Andrews is noted for his photographs of one-room schoolhouses in southern Lane County, 1911�1913. The collection was donated by Andrews� sisters, Martha Belknap and Dorothy Brown, in March 1955.
