<!-- Article Text -->
These are phonetic (Soundex) indices to birth certificates used to locate the records. The indices were compiled by the Works Projects Administration in the 1930s. Arrangement is by Soundex code number. Within an individual code number, volumes are indexed by the father's first given name. The information includes the child's name, the mother's maiden name, the child's sex and birth date, the county of birth, the file number, the cross index name, and the father's name.
