Utah Federal Resettlement Project Oral Histories
<!-- Article Text -->
These oral histories are related to the Federal Resettlement Project in Utah, which was part of a broader New Deal resettlement project in the United States during the Great Depression. The program sought to make better use of the nation's natural resources by reassigning poor farmers from marginal to productive land. The undertaking was supervised by various governmental departments including the Federal Emergency Relief Administration, the Resettlement Administration, and the Farms Security Administration from the 1930s until the early 1940s.<br/>This collection contains 11 oral histories pertaining to the Federal Resettlement Project and the Great Depression in Utah. Brian Q. Cannon conducted these oral histories for his 1986 master's thesis, Remaking the Agrarian Dream: The New Deal's Rural Resettlement Program in the Mountain West. These recordings include interviews about the Green River Drought Pumping Project, resettlement in Benmore, Utah, resettlement in Widtsoe, Utah, and the Leota Bottoms Drought Pumping Project. There is a finding aid for this collection.<br/>Funding for the digitization of these materials was provided, in part, by the Utah Department of Heritage and Arts.
