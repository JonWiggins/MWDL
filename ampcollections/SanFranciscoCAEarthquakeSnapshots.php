<!-- Article Text -->
</p><br/>A few minutes after five o'clock in the morning on April 18, 1906, huge shockwaves began wreaking havoc on the city of San Francisco, California, and in the immediate aftermath, fires spread across the area. The combination of the earthquake and the fires destroyed over 80 percent of the city.</p><br/> At the time of the earthquake, John Lorin Taylor, a native of Ogden, Utah, was traveling in San Francisco (possibly as a missionary for the Church of Jesus Christ of Latter-day Saints) and witnessed the incident.This digital collection contains the 25 photographs that Taylor took on the day of the earthquake and the two days following. The images portray the devastating toll that the earthquake and subsequent fires took on the city and its inhabitants, over 3,000 of whom were killed, and more than 225,000 left homeless. The photographs include images of refugees as well as several notable buildings such as the Emporium, James Flood, the Examiner, the Call, Merchant’s Exchange building, Hibernia Bank, City Hall,and the Central Bank – all of which were destroyed during the disaster. </p><br/>
