Tooele County (Utah) Register of Deaths
<!-- Article Text -->
This series contains a register used to record deaths in the county for the years 1898-1905. Each entry may include such information as the decedent's name, date and place of death, length of residence in the county, sex, color, race or nativity, age, occupation, cause of death, marital status, birth place, last place of residence, the name of the informant making the report, and an assigned reporting number.
