<!-- Article Text -->
"Photos and other images documenting the career of silent film star/producer Nell Shipman, who made many of her motion pictures in Idaho in the 1920s.<br/>The photographs in the Nell Shipman digital collection were, with a few exceptions, donated to Boise State University by Shipman's eldest son, Barry, in 1988. They date mainly from the latter years of her filmmaking career (1920-1924), but there is a sprinkling of earlier and later images. Particularly well represented by stills are her films The Girl From God's Country (1921) and The Grub-Stake (1923).  So too are her years at Priest Lake, Idaho, where she made several short films known collectively as The Little Dramas of the Big Places. No known copy of The Girl From God's Country is known to survive, so the stills are all that are left to document that film."
