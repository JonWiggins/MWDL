<!-- Article Text -->
Photos take by Claudio Beagarie of Cesar Chavez and the farmworkers movement in California in the 1960s. From 1965 to 1967, photographer Claudio Beagarie documented through photos the farm workers' struggle for economic justice and civil rights in California. A union activist with a commitment to progressive causes, Beagarie captured images of farm workers in their homes, in the fields, and marching with Cesar Chavez, cofounder of the United Farm Workers of America and the best-known leader of the movement.
