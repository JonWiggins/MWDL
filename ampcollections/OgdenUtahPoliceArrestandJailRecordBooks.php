Ogden (Utah) Police Arrest and Jail Record Books
<!-- Article Text -->
This series contains various record books documenting arrests made, and prisoners held, by the Ogden City Police Department. Arrest Record Books and Record of Prisoners Books include: name of person arrested, name of arresting officer, time and place of arrest, charge, and fine or punishment given. The Criminal Record Books and Prisoner Identification Records document individuals held by the police department and may include a prisoner number, mug shot, and the prisoner's physical description.
