Capitol Commission Capitol Design Competition Rendering
<!-- Article Text -->
In order to select an architect to design Utah's State Capitol building, the Capitol Commission sponsored a design competition. Architects who wished to participate were required to demonstrate that they possessed the necessary expertise by submitting examples of their work. Those that were approved to participate received a Program of Competition outlining the rules of the competition and the design program for the proposed capitol.
