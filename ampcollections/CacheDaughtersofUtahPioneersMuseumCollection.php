Cache Daughters of Utah Pioneers Museum Collection
<!-- Article Text -->
The Cache Daughters of Utah Pioneers Museum honors the pioneers who came to Utah and settled Cache Valley before 1869. It is the Museum's mission to honor that legacy by sharing the artifacts, histories and photos of those pioneers. The Museum operates under the direction of the International Society Daughters of Utah Pioneers.<br/>The portraits in this digital collection represent pioneers from around the world who came to Utah. The portraits depict early settlers of Cache Valley, Utah, prominent business leaders in Cache County, and early leaders of the Church of Jesus Christ of Latter-day Saints. They also depict community organizations in Cache Valley, including local bands, fire departments, the Cache Stake/Logan Tabernacle Choir, and Logan Temple workers. <br/>Funding for the digitization of these materials was provided, in part, by a grant from the Utah State Historical Records Advisory Board (USHRAB).
