<!-- Article Text -->
</p><br/>This collection contains large-scale, detailed maps from 1867 into the early 20th century depicting the commercial, industrial, and residential sections of many Utah cities. They were designed in 1866 by surveyor D.A. Sanborn to assist fire insurance agents in determining the risk associated with insuring a particular property. The map surveyors meticulously documented the structural evidence of urbanization - building by building, block by block, and community by community.</p><br/>
