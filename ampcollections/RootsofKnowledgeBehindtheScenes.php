<!-- Article Text -->
On November 18, 2016, in the Bingham Gallery of UVU's Fulton Library, an amazing stained glass windows project was unveiled. Called Roots of Knowledge, it consists of 80 individual stained glass windowpanes, spanning 10 feet in height and 200 feet in length. The project was created by Tom Holdman and the creative artists at the Holdman Studios in Lehi, Utah, and aims to trace, in glass, the development of human knowledge, from the genesis of life on Earth to hope for the future. This digital collection, called The Roots of Knowledge Project: Behind the Scenes, is comprised of photographs taken by staff of the Fulton Library's George Sutherland Archives of the creative process and painstaking work that went on at the Holdman Studios during the construction of these beautiful windows. The steps from start to finish are documented.
