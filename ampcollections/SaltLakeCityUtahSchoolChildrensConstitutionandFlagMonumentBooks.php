Salt Lake City (Utah) School Children's Constitution and Flag Monument Books
<!-- Article Text -->
This series contains two books compiled by the school district to document and commemorate the erection of the School Children's Constitution and Flag Monument on the west side of Washington Square in front of the Salt Lake City and County Building. The monument was completed in 1937 and included a flag pole with a sculpture of two children with the United States Constitution standing at the base, and one of the children pointing up toward the flag. School children donated money to fund the monument and local children acted as models for the sculpture. In 1936 each school in the city compiled a list of students and what occupation each aspired to when they grew up. These lists were sealed in a time capsule in the monument when it was dedicated in 1937.<br/>The books in this series were compiled after the time capsule was opened in 1952. They include copies of newspaper articles about the erection of the monument and photographs of the dedication in 1937 and the opening of the time capsule in 1952. They also contain documentation of efforts to erect a flag pole not only at the City and County Building, but at each school in the district as well.
