<!-- Article Text -->
The Polk Directories for Ogden Utah span from 1870 to 2016. The city directory will contain an alphabetical list of citizens, listing the names of the heads of households, their addresses, and occupational information. Sometimes a wife's name will be listed in parentheses or italics following the husband's. Other helpful information might include death dates for individuals who had been listed in the previous year's directory, names of partners in firms, and forwarding addresses or post offices for people who had moved to another town.
