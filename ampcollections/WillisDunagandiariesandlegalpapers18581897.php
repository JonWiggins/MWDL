<!-- Article Text -->
Willis Dunagan was a farmer in Marion County, Oregon. The collection includes 15 bound diaries (1865�1872, 1875, 1879�1884, 1886�1891, 1894�1897) that include daily activities, financial records, and occasional inspirational musings. The collection also includes a memorandum of a cattle drive from Camp Floyd, Utah, to Meadow Creek, California, August 16-October 4, 1869 [?], by an unknown diarist. The papers include legal documents relating to the estate of Israel Chamness of Marion County, 1859�1861. The collection also contains two (rolled) diplomas earned by Dunagan, one high school, and one from the Oregon Normal school.
