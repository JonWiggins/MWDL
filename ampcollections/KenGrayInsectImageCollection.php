<!-- Article Text -->
The Ken Gray Insect Image Collection consists of prints and negatives depicting various types of insects at their larval and adult stages.<br/>In his capacity as a pest specialist with the Pacific Supply Cooperative of Portland, Ken Gray photographed insects as part of a project to create a library of images. Assisted by OSU entomologists in the identification of insects, Gray donated these images to the OSU Extension Service in the mid-1970s. In a project funded by the Environmental Protection Agency, the OSU Extension Service generated slides from 4300 of the Gray insect images and marketed them as aids in the certification of pesticide applicators to university entomology departments.
