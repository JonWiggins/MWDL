Joanna Russ, feminist, educator, award-winning author and literary critic was born February 22, 1937 in New York City.ï¿½The Joanna Russ papers include manuscripts of her novels, unpublished plays, published and unpublished short stories, theoretical essays, and sketches of contemporary life. Russ also maintained an extensive correspondence with many U.S. and English feminist writers and theorists of the late twentieth century as well as with former university colleagues and students and with those involved in science fiction. The collection offers a view of the evolution of thought among a sizable group of women intellectuals as they attempted to conceptualize feminism and to apply feminist perceptions of power in society into the realities of their own lives.
