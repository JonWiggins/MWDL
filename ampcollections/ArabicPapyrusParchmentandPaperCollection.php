</p><br/>The Arabic Papyrus, Parchment & Paper Collection is the largest of its kind in the United States, containing 770 Arabic papyrus documents, 1300 Arabic paper documents, and several pieces on parchment. The collection was acquired by Prof. Aziz Suriyal Atiya, founder of the Middle East Center and the Middle East Library. A large number of pieces date to the period between 700 and 850 CE. The collection includes a significant number of documents from the pre-Ottoman period and thus offers unique source material on the political, economic, religious and intellectual life of Egypt during the first two centuries of Islamic rule and the period up to Ottoman domination.</p><br/>
