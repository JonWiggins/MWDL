<!-- Article Text -->
<p></p><br/><p>This series contains the official copies of executive orders and proclamations issued by the Governor. Executive orders afford a method for reacting to and preparing a plan of action for sudden problems or changing events which require the Governor's prompt attention. Their issuance provides an established conventional means of responding to these events. Through the executive order, the Governor also creates mechanisms to implement the regulations and establish organizations to perform activities of the government in some specific way. Proclamations are more broad statements of position. They provide a formal means to report to the general public that the Governor has responded to an event in a specific manner. Frequently proclamations are issued to pay tribute or to express formal observance of holidays or other noteworthy occasions.</p><br/></p>
