Utah Department of Health Sheep Radiation Studies
<!-- Article Text -->
The records in this series include correspondence, memorandum, meeting minutes, photographs, and reports from the various investigators and agencies involved with the sheep death investigations that took place in Cedar City in 1953 and 1954. The study papers and compiled reports found here were collected by the Utah Department of Health, and highlight the questions surrounding radioactive fallout from the Nevada Test Site and its contributing role in the heavy losses of sheep in 1953 among herds that were wintered in areas downwind of the site.
