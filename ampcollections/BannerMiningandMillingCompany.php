<!-- Article Text -->
Letters and other documents received by Henry K. Doll, of Scranton, Pennsylvania, from officers of the Banner Mining and Milling Company relating to his investment in and operations of the Banner Mine in Silver City, Idaho. The majority of the letters are written from Peter Steele, president of Banner. In his letters Steele details the challenges of operating a mine and mill in Silver City. From 1912 to 1923, the mine experiences a list of problems and catastrophes that stop work, and the miners rarely find a significant deposit of silver ore. Steele spends most of his time seeking investors to buy stock and bonds to finance mining operations. This timeline demonstrates the ups and downs the mining company experienced from 1912 to 1923.
