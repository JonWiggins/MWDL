Utah State University Statesman Photograph Collection
<!-- Article Text -->
The USU Statesman is a student-run campus newspaper. The USU Statesman Photograph Collection consists of mostly black and white prints and 35mm negatives of various activities relating to Utah State University. Included are images of sports, student activities, buildings, ROTC, and USU faculty and staff. The photographs in this collection were taken between 1975 and 1999 for the USU Statesman by student photographers such as Mike Slade, Eli Lucero, and Darin Oswald.
