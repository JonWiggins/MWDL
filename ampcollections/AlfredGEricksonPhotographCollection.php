</p><br/>Cache Valley native Alfred Gordon Erickson attended Utah State Agricultural College in the second half of the 1940s following his service in the Army Air Force during World War II. The 255 black and white photographs in this digital collection were taken by Erickson between 1940 and 1950 while he was with the Army in California and a college student at USAC in Logan, Utah. Of particular interest are several views of Jack London's ranch, visited by Erickson while he was based in California.</p><br/>
