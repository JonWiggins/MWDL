<!-- Collection Description -->
This series contains civil case alphabetic indexes created by the Second District Court in Weber County to record the names of the parties involved in civil actions and provide assigned case numbers, facilitating clerical management of and access to the case files. Volumes in the series alternate listings on each page, with the left page listing entries by surname of the plaintiff and the right page by surname of the defendant. Specific information recorded for each case includes the docket number (case number) as well as the corresponding register of action book.
