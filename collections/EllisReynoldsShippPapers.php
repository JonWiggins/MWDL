<!-- Collection Description -->
</p><br/>The Ellis Reynolds Shipp papers came to the Historical Society in two installments (1967 and 1973) as a gift of the children of Joseph White and Ellis Shipp Musser. Besides the Shipp material, a quantity of Musser family material was also donated. The Shipp papers are completely open to researchers. Dr. Shipp's papers contain a great deal of correspondence, all original holographs. It has been arranged according to writer, then chronologically. The biggest division is, of course, that of Ellis Shipp herself. There are smaller groups of letters written by her children, husband, and his other wives.</p><br/> Another major part of the collection is the poetry of Dr. Shipp. She wrote verses continually for most of her life and published a volume of her poetry in 1910, titled Life Lines. She also wrote a poem for Christmas each year as a Christmas card. Besides poetry and correspondence, the collection includes a great deal of biographical material on the Shipps and Hawleys. There are also examples of diaries kept by Dr. Shipp, as well as several editions of her autobiography which was compiled and published by Ellis Shipp Musser in 1962.</p><br/>Subjects covered, at varying depths, by the Shipp papers include early medical practice, genealogy and family history, polygamy, and life in nineteenth and early twentieth century Utah.</p><br/>
