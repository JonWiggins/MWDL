"</p><br/><em>“Let us remember that there is an ethical element in sound land use. To us as human beings—rather than as animals—this ethical element may well be as important as the element of survival.”</em> -- Lincoln Ellison in his 1955 inaugural address ""Our Weight in the Balance of Nature"" to the Utah Academy of Sciences. <br><br> Ellison’s extraordinary passion for nature and the outdoors shines clearly in his journals, correspondence, and scientific writing. A diarist from a young age, Ellison grew up in an age of letter writers and was himself an eloquent writer. Those of his writings included in this digital collection show the thinking of an early ecological pioneer. They chronicle his ideas about man and the environment, recreation, wilderness and family life. They also represent a narrative about the western United States during the Depression and World War II. <br><br> Born in Portland, Oregon, Ellison grew up in California and worked for the United States Forest Service throughout the western United States during the 1930s-1950s. He was the director of the Great Basin Experiment Station in Ephraim, Utah and later moved to Ogden to manage the region’s range research program.This collection was generously donated to Utah State University by his daughter, Liane Norman, whose full biography of Ellison entitled Lincoln Ellison Director, Great Basin Branch Experiment Station 1938—45 is included. </p><br/>"
