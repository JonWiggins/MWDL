<!-- Collection Description -->
"</p><br/>Monument Valley, with skyward reaching red rock monuments, has long been symbolic of the rugged deserts of the American West. However, before Western films, television shows, and commercials established its fame, Monument Valley was virtually inaccessible for locals and tourists. Even after the invention of the automobile, the absence of an effective road system in the harsh terrain did little to heighten its accessibility. Dolph Andrus, of Bluff, Utah, set out to change that and was convinced that he could open Monument Valley to automobile tourism.</p><br/>In the Spring of 1917, Andrus left Bluff, Utah with William H. Hopkins, a dentist and enthusiastic photographer, to complete an automobile journey through Monument Valley, on to Kayenta, Tuba City, and Lee’s Ferry, Arizona. Later that summer, Andrus set off again with photographer, L. W. Clement with the intent to photograph the natural bridges and monuments of the valley. Andrus’s wife, Irene, and daughter, Torma, accompanied them on this trip in which burros were used for transportation. </p><br/>The bulk of this collection consists of the photographs taken during these tours of Monument Valley, Natural Bridges National Monuments, Zion National Park, the Colorado River, and the San Juan River. Andrus's Maxwell automobile is featured in many of the images. Also in the collection is the personal history of Dolph and Irene Andrus and log of these trips. The Monumental Highway Digital Collection is comprised of the Dolph Andrus Monumental Highway Photograph Collection (P0360) and the Dr. William H. Hopkins Addendum Collection (P0542). </p><br/>Please visit the <a href=http://usudigital.lib.usu.edu/exhibits/show/dolph-andrus-s-monumental-high"""">Dolph Andrus's Monumental Highway Expeditions Digital Exhibit</a> for more information. </p><br/><a href=""http://digital.lib.usu.edu/cdm/singleitem/collection/monumental/id/38/rec/14"" target=""_blank"">Torma at the wheel of her first car</a></p><br/></p>"""
