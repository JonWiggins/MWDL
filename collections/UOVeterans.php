<!-- Collection Description -->
The UO Veterans Oral History Project was established in 2012 with the aim of documenting the military experiences of men and women associated with the University of Oregon and the wider Oregon community. The project understands military experience broadly to include not just combat but all aspects of military service. The UO Veterans Oral History Project rests on interviews with both veterans and active duty service men and women. Interviews are conducted and transcribed by University of Oregon students enrolled in a class designed to support the project. The interviews are permanently housed in the archive at the University of Oregon Libraries.
