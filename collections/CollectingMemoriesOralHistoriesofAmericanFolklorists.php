<p></p><br/><p>Collecting Memories features the voices of American folklorists and aims to document the development and change in the field of folklore since the mid-twentieth century. A joint project of the American Folklore Society (AFS) and Utah State University (USU) (official repository of the AFS archives), this collection offers a descriptive listing of interviews held at USU and at other institutions. The USU interviews include transcript, sound, photographs and more. Interviews housed at other institutions are mostly analog and not available online, however, links lead users to descriptive records at the owning institutions where in some cases transcripts are available.</p><br/></p>
