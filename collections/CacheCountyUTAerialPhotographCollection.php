"</p><br/>The Cache County Aerial Photograph Collection consists of high altitude images probably taken in 1952 and 1959 by the Aerial Photography Field Office in Salt Lake City. Before 1978 a number of federal agencies conducted high altitude surveys. In 1978 the National High Altitude Photography program was created to consolidate federal high altitude photography activities to save money. The images in this collection were probably used as part of the Soil Conservation Survey.</p><br/>The collection also includes images of Cache County, as well as images along the Cache/Box Elder County and the Cache/Weber County boundaries in Utah and from Oneida and Franklin County on the Utah/Idaho border. These images are from 9"" x 9"" negatives and taken at 1:20,000. The date the image was taken is usually in the upper left-hand corner.</p><br/>"
