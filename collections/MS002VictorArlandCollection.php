</p><br/>This collection consists of a series of original letters written by Victor Arland (c. 1848- 1890) of Arland, Wyoming to Mr. C.P. Dadant of Hamilton, IL, a beekeeper and manufacturer of bee supplies and distributor of honey. The letters are handwritten in French, dated between 1872 and 1889, and for the most part are in good to excellent condition. The letters have been flattened, separated by acid-free, lignin-free paper in archival folders and housed in Box 1, organized as Series 1. Included in Series 2 are excerpts of essays that have resulted from research with this collection.</p><br/>
