<!-- Collection Description -->
"The U.S. Presidential Election Reflections collection launched on 20 January 2017, just in time for the United States presidential inauguration. This social media collecting project boasts 233 reflections about the 2016 presidential election results. The collection project began the day after the election and ran for one month. The respondents are from 27 U.S. states and four foreign countries.<br/>The 2016 presidential election exposed the depths of a political divide for much of the country and the world was watching. Whether people were encouraged or discouraged by the election results, USU Special Collections (SCA) curators wanted to collect the reflections of this momentous national event. It is important for future generations to know what people were thinking and feeling.<br/>The social media collecting project is a first for the USU Library. It was conceived and created the day after the election as the world watched social media ""blow up"" with personal reflections on the election results. It seemed important to collect and preserve these reflections, and fast.<br/>Using social media to promote the project, SCA curators and USU librarians used Qualtrics software to create a survey that would allow interested participants to add their social media reflection to the project. The survey was open from 9 November to 9 December 2016. Those who contributed to the project were able to do so quickly and all online. Survey respondents were asked to share their thoughts on the election and had the option to provide additional information about themselves.<br/>Information provided in the survey results helped USU librarians organize the collection and will allow others to be able to study how reactions varied in different locations or between men and women, etc.<br/>Although the project received responses from around the world, the Library collected the reflections as a part of its mission to gather the voices and perspectives of members of Northern Utah communities. SCA hopes to do this for coming presidential elections, and possibly state elections. Future researchers seeking to understand the diversity of the political culture of Utah will require more than vote counts. Like tiles in a mosaic, each voice adds shape and dimension to our national identity."
