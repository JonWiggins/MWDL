<!-- Collection Description -->
This collection of historic negatives from the Gertrude Bass Warner Collection at the Jordan Schnitzer Museum of Art represents a significant portion of the museum's early collection photo documentation. The majority of these images were taken between approximately 1933 and 1941, during a concerted survey of the collections. The photographs are, in some cases, the earliest record of the condition and display environment of the foundational works of the Murray Warner collection which focuses on Chinese, Japanese and Korean works although it also contains smaller numbers of objects from other Asian cultures. These images also include a few interesting additions from the 1950's and 1960's such as 20th century Canadian sculpture, German prints, and Austrian lithographs. This project was a collaborative effort between the Jordan Schnitzer Museum of Art (JSMA) and the University of Oregon Libraries. In 2014 the JSMA sought and received an National Endowment for the Humanities (NEH) Preservation Grant. The Grant funds helped to support the material and labor costs to inventory, digitize and rehouse the collections in cold storage. The negatives were rehoused in archival materials and now reside in the archival cold storage facility at the Knight Library.
