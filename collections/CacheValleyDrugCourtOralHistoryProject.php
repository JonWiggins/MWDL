<!-- Collection Description -->
"In 2000, Judge Thomas Willmore of Utah's First District Court, with help from Brock Alder at Bear River Health Department, established a drug court for Cache County: the fifth in Utah. In 2001 they established the drug court in Box Elder County. The concept of drug courts began in 1989 in Miami-Dade County, Florida, in a response to the growing crack cocaine drug problem plaguing Miami. Today there are approximately 1,561 drug courts in the United States. These specialized courts are problem-solving courts bringing together the judiciary, prosecution, defense, adult parole and probation, law enforcement, mental health, social services, treatment communities (like 12-step programs), and the greater community (including former drug court participants) to aid addicted offenders with long-term recovery as opposed to incarceration.<br/>In May 2016, Andrew Dupree, a Cache Valley Drug Court participant (and now graduate), approached USU Folklore Curator Randy Williams with the idea of partnering on an oral history effort to capture the voices of people involved in the drug court experience. Andrew Dupree attended a May 2015 presentation Williams hosted with students from the USU/Library of Congress Cache Valley Refugee Oral History Project. He wanted people in the drug court community (addicts, Cache Valley Drug Court Committee members, and family members) to have a similar public voice. Together with Jennifer Duncan, Special Collections Librarian, Alder, Andrew Dupree and Williams, launched the Cache Valley Utah Drug Court Oral History Project in November 2016.<br/>The project ran from November to May 2017. This collection is the outgrowth of the project and includes 26 interviews, both audio and transcript, with 28 people. Each participant had the opportunity to review their interview transcript. The interviews include the voices of drug court participants and graduates, family members, and professionals associated with drug court. The majority of the interviews were conducted at the Logan Library, the host for the 24 May evening event: ""Voices from Drug Court"" that highlighted the project.<br/>This project received a grant to support transcription from the Utah Humanities & Utah Division of State History."
