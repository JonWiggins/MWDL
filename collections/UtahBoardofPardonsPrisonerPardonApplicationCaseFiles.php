</p><br/>Case files consist of letters to the Governor, a formal application for a pardon, petitions and letters of support from the public and officials connected to the case and during the first 40 years, case files often contained court transcripts, biographical sketches, prison evaluations and a wide variety of related documentation. Cases illustrate the process of review by the board of cases of prisoners incarcerated in the Utah prison system to determine if they should be released before their regular sentence ended. Documents contain personal data about the prisoner, criminal activity, family background and evaluation of the prisoner's adjustment to incarceration.</p><br/>&nbsp
