"Orla G. Allen (dates unknown), commonly known as O.G. Allen, was a professional photographer operating in Pendleton, Oregon, circa 1910-1920. ""Electric Studio"" is a franchise name that appeared in locations throughout the West. The collection documents rodeo people and activities, and a Pendleton event. Unknown. Possibly a donation from Lee D. Drake of Pendleton."
