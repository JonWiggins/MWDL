</p><br/>The Millennial Star was the longest running Latter-day Saint (LDS) periodical, published continuously for 130 years until discontinued in 1970 with the overhaul of all the LDS magazines. It was inaugurated by the Twelve at the beginning of their great mission to England. Brigham Young and his fellow members of the Twelve landed in Liverpool on April 6, 1840, the tenth anniversary of the Church. Eight days later they began a series of meetings in Preston in which they resolved to publish a monthly periodical to be called the Latter-day Saints Millennial Star.</p><br/>
