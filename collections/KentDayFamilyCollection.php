<!-- Collection Description -->
The Kent Day Family Collection (1917-1919) contains photographs and letters collected by different day family members, predominantly Ada Elizabeth Day. This collection documents the beginning of World War I from the perspective of different soldiers stationed in various places. The collection also includes documentation of the death of David Day who died of Spanish Influenza.
