"<p></p>
<p>In the 1860s and 1870s the Intermountain West for most Easterners was an exotic, interesting, and potentially dangerous place. Eager to see images, but distrusting the accuracy of artists, they clamored for the realism that photography could provide. William Henry Jackson, A.J. Russell, C.R. Savage, Jack Hillers, Carleton Watkins, and others used the transcontinental railroad to transport the bulky equipment needed for collodion, wet-plate negatives that brought top dollar in the form of stereo-views or ""large-format"" prints. The twenty years from 1860 to 1880 was the golden age of Western photography because quality images were in high demand. Every government survey and all the major railroads had official photographers. Interest would wane, however, in the 1880s as the general public started to want cheap, low quality shots of popular tourist destinations. Furthermore, as the West became mundane, the more evocative, romantic recreations of the ""Old West"" found in dime novels and Wild West shows became more popular. </p>
