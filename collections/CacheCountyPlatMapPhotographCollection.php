<!-- Collection Description -->
This collection consists of reproductions of 49 original plat maps in Cache and Franklin counties, showing city lots as well as property boundaries for agricultural land. The plats were drawn by Cache County surveyor James Henry (J.H.) Martineau in the 1860s and 1870s. James Henry Martineau (1828-1921) was a Mormon convert who is perhaps best known as a territorial, federal, and railroad (Union Pacific) surveyor. In addition, he was a settler, clerk, cartographer, photographer, and engineer. He completed a variety of surveys in southern Utah before serving as an officer in the Nauvoo legion during the Utah War of 1857-58. In 1860 he settled in Cache Valley, working as the county surveyor and the U.S. deputy internal revenue collector. In 1888 he moved his families to Colonia Juarez in Mexico, although in 1903 he moved back to Salt Lake City. He died on June 24, 1921 in Salt Lake City at the age of ninety-three.
