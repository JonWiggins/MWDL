<!-- Collection Description -->
This series contains photographs which functioned as documentation of the construction of the State Capitol. The majority of the series contains pictures of the finished capitol building, ground breaking ceremony, initial excavation of the construction site, and individuals involved in the construction process. The collection also holds a unique commemorative photograph album produced by Shipler's Commercial Photographs of Salt Lake City which was presented to commission members.
