<!-- Collection Description -->
"</p><br/>Las Vegas, Spanish for ""the meadows,"" was always about water. Its natural springs were a watering stop, for wandering Native Americans, Spanish and American traders, Mormon settlers, ranchers, and Senator William Clark and his San Pedro, Los Angeles & Salt Lake Railroad. As the railroad depot grew into a city, the pressure upon the water supply would be a permanent aspect of development. The collection presented here documents this history back to the 19th century military and scientific surveys conducted by the U.S. government, directed ultimately toward the future settlement and exploitation of the West. Associated with the scientific expeditions to map a route for the transcontinental railroad were studies to explore the feasibility of irrigation to support agriculture.</p><br/>"
