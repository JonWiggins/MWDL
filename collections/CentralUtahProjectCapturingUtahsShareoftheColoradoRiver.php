<!-- Collection Description -->
</p><br/>In this digital collection of 71 oral histories, stakeholders from the Central Utah Water Conservancy District (CUWCD), Central Utah Project Completion Act (CUPCA), the Bureau of Reclamation (BOR) and the Ute Nation, join with Utah water users, members of the environmental community and Utah’s political establishment to tell the complex and oftentimes controversial story of the Central Utah Project (CUP). The CUP captures the excess flow of the Duchesne River and its tributaries in the Colorado River Basin of eastern Utah and transfers it to the Wasatch Front through a trans-mountain diversion consisting of pipelines, tunnels and reservoirs. Originally under the purview of the BOR, Congress transferred authority to complete the project to the CUWCD when it passed the unprecedented Central Utah Project Completion Act (P.L. 102-575) on 30 October 1992, which created the Utah Reclamation Mitigation & Conservation Commission to assure environmental compliance. As a result of this legislation the Interior Department created the CUPCA office for oversight.</p><br/>Commissioned by the CUWCD and CUPCA, the oral history project is an effort by Utah State University Library’s Special Collections & Archives to preserve and present this fascinating story and to augment research for the forthcoming book on the CUP by Utah historians Craig Fuller, Robert Parson and Ross Peterson. Randy Williams, USU Fife Folklore Archives Curator and oral history specialist, conducted the interviews (June 2012 - September 2013). </p><br/>
