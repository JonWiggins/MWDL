<!-- Collection Description -->
This series contains civil case alphabetic indexes created by the Court to record the names of the parties involved in actions and provide assigned case numbers, facilitating clerical management of and access to the case files. The indexes contain plaintiff name, defendant name, register of action book number and case file number. Each index contains an alphabetic listing by plaintiff and by defendant.
