<!-- Collection Description -->
The entire photograph collection of Frank and Bethine Church, spanning their young adult years in Boise, Idaho, 1940-1943, the early political career of Frank as Idaho Legislature, 1952-1956, and U.S. Senator, 1958-1980, and Bethine's involvement in social and environmental issue in the 1990s to 2010. The majority of the photographs document the many campaigns, political events in Washington D.C. and around the world, and daily work as a U.S. Senator. Church's involvment in foreign relations is well documented in photographs, as well as his work with other various Senate committees.
