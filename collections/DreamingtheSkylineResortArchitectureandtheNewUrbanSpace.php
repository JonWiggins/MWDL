</p><br/>From the El Rancho to CityCenter, the Las Vegas Strip with its mega-resort hotel/casinos has developed a distinctive architectural style and type, which has now been copied and exported to other gaming and resort cities throughout the world. Not only has this architecture transformed the physical environment of Las Vegas, it has become a leitmotif for post-modern architecture. Aesthetically eclectic, it has become a hallmark of the themed amusement park that characterizes the new urban entertainment and recreational space.</p><br/>
