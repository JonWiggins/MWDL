<p></p><br/><p>The Utah Office of Vital Records and Statistics began issuing death certificates in 1904. A death certificate is the permanent legal record of an individual's death. Each death certificate includes the decedent's full name, date of death, county where death occurred, decedent's race and gender, place and date of birth, marital status, occupation, permanent residence, place and date of burial, time of death, chief cause and contributory factors of death, and if applicable, where illness was contracted and duration of illness. Death certificates also include the names and birthplaces of parents.</p><br/></p>
