<!-- Collection Description -->
"Willie Ottogary was a farmer and performed many duties as a Shoshone chief, but he is most well known in the ""white world"" for his letters which appeared regularly in The Journal (a Logan, Utah Newspaper) from 1909-1929. According to historian Matthew Kreitzer:<br/>""He [Ottogary] wrote over 400 letters during a literary career spanning 23 years. His letters appeared most often in The Journal, but he also was published in the Tremont Times, Oneida County Enterprise, and the Box Elder Journal. Willie's columns reflected a wide range of topics such as agriculture, the LDS Church [Church of Jesus Christ of Latter-Day Saints], his travels, the comings and goings of his people, land issues, hunting and gathering, Sun Dances, social activities, sports, special occasions, economic development, life-cycle events, and new technological innovations. He kept the pulse of Washakie and accurately reported his perceptions of the things that, he believed, mattered most. (Kreitzer 57)""<br/>Willie was the son of O-Ti-Cot-i, who later changed his name to Peter Otahgary after his conversion to the LDS Church. Though his birth date is uncertain, he was baptized a member of the LDS Church on August 1, 1875. He married twice and fathered three children who survived to adulthood. Beginning as early as 1915, Ottogary was chief of the Shoshone at Washakie, the name of the land in Box Elder County, Utah, allotted by the LDS Church for the settling of the converted Shoshone. Ottogary died in March of 1929.<br/>The Willie Ottogary Collection includes copies of newspaper articles from the The Journal (1909-1929), Box Elder Journal (1925), and the Oneida County Enterprise (1924). These articles are letters written by Willie Ottogary to the various newspapers describing the events and well-being of the Shoshone people in Washakie. Also included in this collection are some copies of later correspondence (1929-1959) dealing with efforts to establish a reservation for the Shoshone in Idaho. In addition to the digital collection, there is a finding aid available online."
