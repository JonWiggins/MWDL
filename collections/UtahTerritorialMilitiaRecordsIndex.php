<!-- Collection Description -->
These papers document the administration and activities of various segments of the territorial militia, also known as the Nauvoo Legion. They were brought together by archives staff in the late 1950s and early 1960s in an effort to collect all documents about Utah veterans from any source and to serve as a state repository for those documents.<br/>The Military Records Center acquired the documents from the late 1950s into the early 1960s, rearranging and grouping them by document type, date, and/or military district. Indexes were prepared for most of the correspondence, but plans to index all documents were never completed.
