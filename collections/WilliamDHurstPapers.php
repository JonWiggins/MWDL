<p></p>
<p>U.S. Forester William Daly Hurst's distinguished service with the Forest Service spanned four decades (1937-1976) of changes in policies related to multiple use and resource conservation, including the grazing permit controversy of 1957-1958. The William D. Hurst Digital Collection features Hurst's daily diaries, files, and calendars, providing an in-depth look at his life and work in the Forest Service.</p>
<p> Born in Parowan, Utah, on October 15, 1915, Hurst grew up in Panguitch, Utah, leaving in 1934 to attend Utah State Agricultural College in Logan, Utah. While still earning his B.S. in Forestry there, Hurst began his career with the Forest Service as an Administrative Guard for the Wasatch National Forest in 1937; he became the District Ranger for the Manila Ranger District, Ashley National Forest in 1942. World War II Army service in Japan interrupted his forestry work from 1945 to 1946. After discharge he returned to the Forest Service as Staff Officer of the Cache National Forest, a position he held until 1950 when he became Supervisor of the Ashley National Forest. </p>
<p>In 1955, Mr. Hurst left the beauty of northern Utah for Washington D.C. where he served as an Assistant Chief for Range Management. He returned two years later to Utah where he replaced Floyd Iverson as Chief of Range and Wildlife Management in Region 4 and subsequently was appointed Deputy Regional Forester for the Intermountain Region in 1962. In 1966, Hurst became Regional Forester for the Southwestern Region, remaining in this position until his retirement in 1976.</p>
<p> Besides reading about Hurst's daily work via his written diaries, files, and calendars, users of this collection can hear Hurst speak about his experiences in Logan Canyon and with the Forest Service through two oral history interviews recorded in 2008 and 2009. </p>
